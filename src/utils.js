/**
 * @param {Array} array
 * @return {Object} object
 */

// This function takes an Object and returns a normalized array
// For example input {a: 1, b: 2, c: 3} will return [1,2,3]
export const convertObjectToArray = inputObject => {
  return inputObject.map(value => value);
};

export const mergeObjects = (objectA, objectB) => {
  return Object.assign(objectA, objectB);
  return objectA;
};
